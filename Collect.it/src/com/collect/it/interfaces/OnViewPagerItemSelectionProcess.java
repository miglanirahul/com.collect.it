package com.collect.it.interfaces;

import android.view.View;

/**
 * This interface is used to detect the item click listener for the view pager
 * and its associated adapter
 */
public interface OnViewPagerItemSelectionProcess {

	/**
	 * The method that will return the selected page/row of the view pager for
	 * the associated class
	 * 
	 * @param view
	 *            view of the selected page in view pager
	 * @param position
	 *            position of the selected view
	 * @param associatedTaskId
	 *            detects the associated view pager so that the further process
	 *            can be proceed
	 */
	void onItemSelected(View view, int position, String associatedTaskId);
}
