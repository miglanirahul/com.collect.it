package com.collect.it.googleplus;

import com.google.android.gms.plus.PlusClient;
import com.google.android.gms.plus.model.people.Person;

/**
 * This model class used to define variables that are used for google plus
 * processing
 */
public class GooglePlusModel {

	/**
	 * Declare class variables
	 */
	private static GooglePlusModel instanceGooglePlusModel;

	public static GooglePlusModel getInstance() {
		if (instanceGooglePlusModel == null) {
			instanceGooglePlusModel = new GooglePlusModel();
		}
		return instanceGooglePlusModel;
	}

	/**
	 * Declare class variables
	 */
	private Person mPerson;

	/**
	 * @return the mPerson
	 */
	public Person getmPerson() {
		return mPerson;
	}

	/**
	 * @param mPerson
	 *            the mPerson to set
	 */
	public void setmPerson(Person mPerson) {
		this.mPerson = mPerson;
	}

}
